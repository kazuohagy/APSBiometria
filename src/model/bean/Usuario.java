package model.bean;

/**
 *
 * @author kazuo
 */
public class Usuario {
    private int privilegio;
    private String nome;
    private byte[] foto;
    
    public Usuario(int privilegio, String nome, byte[] foto){
        this.privilegio = privilegio;
        this.nome = nome;
        this.foto = foto;
    }

    public int getPrivilegio() {
        return privilegio;
    }

    public void setPrivilegio(int privilegio) {
        this.privilegio = privilegio;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public byte[] getFoto() {
        return foto;
    }

    public void setFoto(byte[] foto) {
        this.foto = foto;
    }
    
}
